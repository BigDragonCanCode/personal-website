export const skillsByCategory: SkillCollection[] = skillCategories.map((category) => ({
  category,
  skills: resumeSkills.filter((skill) => skill.category === category.id),
}))

export const skillsById = Object.fromEntries(
  resumeSkills.map((skill) => [skill.id, skill]),
) as Record<string, ResumeSkill>

export const iconReadySkills = resumeSkills.filter(
  (skill): skill is IconReadySkill => skill.iconStatus === 'ready',
)

export const globeSkills = resumeSkills.filter((skill) => !skill.wallOnly)

export const topLevelIconReadySkills = globeSkills.filter(
  (skill): skill is IconReadySkill => skill.iconStatus === 'ready',
)

export const missingIconSkills = resumeSkills.filter(
  (skill): skill is MissingIconSkill => skill.iconStatus === 'missing',
)

export const wallOnlyChildSkills = resumeSkills.filter(
  (skill): skill is WallChildSkill => skill.wallOnly === true && Boolean(skill.parentSkillId),
)

export const wallTopLevelSkills = resumeSkills.filter((skill) => !skill.wallOnly)

export const wallChildSkillsByParentId = wallOnlyChildSkills.reduce<WallChildSkillsByParentId>(
  (groups, skill) => {
    const parentId = skill.parentSkillId
    const existingChildren = groups[parentId] ?? []

    groups[parentId] = [...existingChildren, skill]
    return groups
  },
  {},
)

export const expandableWallParentSkills = wallTopLevelSkills.filter(
  (skill): skill is ExpandableWallParentSkill =>
    Array.isArray(skill.childSkillIds) && skill.childSkillIds.length > 0,
)

export const proxyIconSkills = resumeSkills.filter((skill) => skill.iconMatch === 'proxy')

export const skillsSectionData: SkillsSectionData = {
  allSkills: resumeSkills,
  iconReadySkills,
  missingIconSkills,
  globeSkills,
  wallTopLevelSkills,
  wallChildSkillsByParentId,
  expandableWallParentSkills,
}

